package main

func run(rows []string) uint64 {
	var hash uint64 = 1469598103934665603
	for _, row := range rows {
		var v uint64
		if len(row) == 15 {
			b0 := row[0]
			b1 := row[1]
			if b0 >= 'A' && b0 <= 'Z' && b1 >= 'A' && b1 <= 'Z' {
				b2 := row[2]
				b3 := row[3]
				b4 := row[4]
				b5 := row[5]
				if b2 >= '0' && b2 <= '9' &&
					b3 >= '0' && b3 <= '9' &&
					b4 >= '0' && b4 <= '9' &&
					b5 >= '0' && b5 <= '9' &&
					row[6] == '-' {

					c7 := row[7]
					c8 := row[8]
					c9 := row[9]
					c10 := row[10]
					c11 := row[11]
					c12 := row[12]
					c13 := row[13]
					c14 := row[14]

					if (((c7 >= '0' && c7 <= '9') || (c7 >= 'A' && c7 <= 'F')) &&
						((c8 >= '0' && c8 <= '9') || (c8 >= 'A' && c8 <= 'F')) &&
						((c9 >= '0' && c9 <= '9') || (c9 >= 'A' && c9 <= 'F')) &&
						((c10 >= '0' && c10 <= '9') || (c10 >= 'A' && c10 <= 'F')) &&
						((c11 >= '0' && c11 <= '9') || (c11 >= 'A' && c11 <= 'F')) &&
						((c12 >= '0' && c12 <= '9') || (c12 >= 'A' && c12 <= 'F')) &&
						((c13 >= '0' && c13 <= '9') || (c13 >= 'A' && c13 <= 'F')) &&
						((c14 >= '0' && c14 <= '9') || (c14 >= 'A' && c14 <= 'F'))) {
						v = uint64(b2 + c7)
					}
				}
			}
		}
		hash ^= v + 1
		hash *= 1099511628211
	}
	return hash
}
