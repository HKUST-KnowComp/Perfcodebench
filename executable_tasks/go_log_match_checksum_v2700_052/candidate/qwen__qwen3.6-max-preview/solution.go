package main

func run(rows []string) uint64 {
	var hash uint64 = 1469598103934665603
	for _, row := range rows {
		v := uint64(0)
		if len(row) == 15 {
			r0, r1 := row[0], row[1]
			r2, r3, r4, r5 := row[2], row[3], row[4], row[5]
			r6 := row[6]
			r7, r8, r9, r10 := row[7], row[8], row[9], row[10]
			r11, r12, r13, r14 := row[11], row[12], row[13], row[14]

			if r0 >= 'A' && r0 <= 'Z' && r1 >= 'A' && r1 <= 'Z' &&
				r2 >= '0' && r2 <= '9' && r3 >= '0' && r3 <= '9' &&
				r4 >= '0' && r4 <= '9' && r5 >= '0' && r5 <= '9' &&
				r6 == '-' &&
				((r7 >= '0' && r7 <= '9') || (r7 >= 'A' && r7 <= 'F')) &&
				((r8 >= '0' && r8 <= '9') || (r8 >= 'A' && r8 <= 'F')) &&
				((r9 >= '0' && r9 <= '9') || (r9 >= 'A' && r9 <= 'F')) &&
				((r10 >= '0' && r10 <= '9') || (r10 >= 'A' && r10 <= 'F')) &&
				((r11 >= '0' && r11 <= '9') || (r11 >= 'A' && r11 <= 'F')) &&
				((r12 >= '0' && r12 <= '9') || (r12 >= 'A' && r12 <= 'F')) &&
				((r13 >= '0' && r13 <= '9') || (r13 >= 'A' && r13 <= 'F')) &&
				((r14 >= '0' && r14 <= '9') || (r14 >= 'A' && r14 <= 'F')) {
				v = uint64(r2 + r7)
			}
		}
		hash ^= v + 1
		hash *= 1099511628211
	}
	return hash
}
