package main

func run(rows []string) uint64 {
	const prime = 1099511628211
	hash := uint64(1469598103934665603)

	for _, r := range rows {
		if len(r) != 15 { // fast length guard
			hash ^= 1
			hash *= prime
			continue
		}
		// branchless validity check
		ok := 1 |
			((r[0]-'A')>>4)&((r[1]-'A')>>4)&
			((r[2]-'0')>>4)&((r[3]-'0')>>4)&((r[4]-'0')>>4)&((r[5]-'0')>>4)&
			((r[6]-'0'-1)>>4)&
			((r[7]-'0')>>4)&((r[8]-'0')>>4)&((r[9]-'0')>>4)&((r[10]-'0')>>4)&
			((r[11]-'0')>>4)&((r[12]-'0')>>4)&((r[13]-'0')>>4)&((r[14]-'0')>>4)
		// mask hex digits 0-9 A-F
		ok &= ((r[7]-'0')>>4) | ((r[7]-'9'-1)>>4)&((r[7]-'A')>>4)&((r[7]-'F'-1)>>4)
		ok &= ((r[8]-'0')>>4) | ((r[8]-'9'-1)>>4)&((r[8]-'A')>>4)&((r[8]-'F'-1)>>4)
		ok &= ((r[9]-'0')>>4) | ((r[9]-'9'-1)>>4)&((r[9]-'A')>>4)&((r[9]-'F'-1)>>4)
		ok &= ((r[10]-'0')>>4) | ((r[10]-'9'-1)>>4)&((r[10]-'A')>>4)&((r[10]-'F'-1)>>4)
		ok &= ((r[11]-'0')>>4) | ((r[11]-'9'-1)>>4)&((r[11]-'A')>>4)&((r[11]-'F'-1)>>4)
		ok &= ((r[12]-'0')>>4) | ((r[12]-'9'-1)>>4)&((r[12]-'A')>>4)&((r[12]-'F'-1)>>4)
		ok &= ((r[13]-'0')>>4) | ((r[13]-'9'-1)>>4)&((r[13]-'A')>>4)&((r[13]-'F'-1)>>4)
		ok &= ((r[14]-'0')>>4) | ((r[14]-'9'-1)>>4)&((r[14]-'A')>>4)&((r[14]-'F'-1)>>4)

		var v uint64
		if ok == 1 {
			v = uint64(r[2] + r[7])
		}
		hash ^= v + 1
		hash *= prime
	}
	return hash
}