bool is_valid_utf8_scalar(const std::string& s) {
    const unsigned char* p = reinterpret_cast<const unsigned char*>(s.data());
    const unsigned char* end = p + s.size();
    while (p < end) {
        int n = continuation_count[*p];
        if (n == 0) {
            p++;
            continue;
        } else if (n < 0) {
            return false;
        } else {
            if (p + n >= end) return false;
            for (int i = 1; i <= n; i++) {
                if ((p[i] & 0xC0) != 0x80) return false;
            }
            if (*p == 0xE0 && p[1] < 0xA0) return false;
            if (*p == 0xED && (p[1] < 0x80 || p[1] > 0x9F)) return false;
            if (*p == 0xF0 && p[1] < 0x90) return false;
            if (*p == 0xF4 && (p[1] < 0x80 || p[1] > 0x8F)) return false;
            p += n + 1;
        }
    }
    return true;
}

