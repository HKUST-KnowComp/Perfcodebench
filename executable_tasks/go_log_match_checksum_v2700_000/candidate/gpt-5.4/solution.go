package main

func run(rows []string) uint64 {
	var hash uint64 = 1469598103934665603
	for _, row := range rows {
		var v uint64
		if len(row) == 15 {
			b := row
			if b[0] >= 'A' && b[0] <= 'Z' &&
				b[1] >= 'A' && b[1] <= 'Z' &&
				b[2] >= '0' && b[2] <= '9' &&
				b[3] >= '0' && b[3] <= '9' &&
				b[4] >= '0' && b[4] <= '9' &&
				b[5] >= '0' && b[5] <= '9' &&
				b[6] == '-' &&
				((b[7] >= '0' && b[7] <= '9') || (b[7] >= 'A' && b[7] <= 'F')) &&
				((b[8] >= '0' && b[8] <= '9') || (b[8] >= 'A' && b[8] <= 'F')) &&
				((b[9] >= '0' && b[9] <= '9') || (b[9] >= 'A' && b[9] <= 'F')) &&
				((b[10] >= '0' && b[10] <= '9') || (b[10] >= 'A' && b[10] <= 'F')) &&
				((b[11] >= '0' && b[11] <= '9') || (b[11] >= 'A' && b[11] <= 'F')) &&
				((b[12] >= '0' && b[12] <= '9') || (b[12] >= 'A' && b[12] <= 'F')) &&
				((b[13] >= '0' && b[13] <= '9') || (b[13] >= 'A' && b[13] <= 'F')) &&
				((b[14] >= '0' && b[14] <= '9') || (b[14] >= 'A' && b[14] <= 'F')) {
				v = uint64(b[2] + b[7])
			}
		}
		hash ^= v + 1
		hash *= 1099511628211
	}
	return hash
}
